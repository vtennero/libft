#include "../includes/libft.h"

int	ft_strtrimc2(char const *s, int n)
{
	int	i;
	int	p;

	i = 0;
	p = n - 1;
	while (s[p] == ' ' || s[p] == '\t' || s[p] == '\n')
	{
		i++;
		p--;
	}
	return (i);
}
