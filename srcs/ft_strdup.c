#include "../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	char	*t;
	int		i;
	int		n;

	i = 0;
	n = ft_strlen(s1);
	t = (char *)malloc(sizeof(*t) * n + 1);
	if (!t)
		return (NULL);
	while (i < n)
	{
		t[i] = s1[i];
		i++;
	}
	t[i] = '\0';
	return (t);
}
