#include "../includes/libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*new_list;

	while (lst)
	{
		new_list = malloc(sizeof(t_list));
		new_list = (f)(lst);
		lst = lst->next;
	}
}