#include "../includes/libft.h"

int		ft_atoisign(int sign, long n)
{
	if (sign == -1)
	{
		if (n - 9223372036854775807 > 0)
			return (0);
		else
			return (-(int)n);
	}
	else
	{
		if (n - 9223372036854775807 > 0)
			return (-1);
		else
			return ((int)n);
	}
	return (197);
}
