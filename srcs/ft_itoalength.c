#include "../includes/libft.h"

int	ft_itoalength(int n)
{
	int	p;
	int	i;

	p = n;
	i = 0;
	if (p < 0)
	{
		i++;
		p = -p;
	}
	while (p != 0)
	{
		p /= 10;
		i++;
	}
	return (i);
}
